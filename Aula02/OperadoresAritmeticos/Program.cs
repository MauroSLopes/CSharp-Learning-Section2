            Console.Write("2 - 2 = ");
            Console.WriteLine(2 - 2);

            Console.Write("2 * 2 = ");
            Console.WriteLine(2 * 2);

            Console.Write("2 / 2 = ");
            Console.WriteLine(2 / 2);

            Console.Write("2 % 2 = ");
            Console.WriteLine(2 % 2);

            Console.Write("2 < 2 = ");
            Console.WriteLine(2 < 2);

            Console.Write("2 <= 2 = ");
            Console.WriteLine(2 <= 2);

            Console.Write("2 > 2 = ");
            Console.WriteLine(2 > 2);

            Console.Write("2 >= 2 = ");
            Console.WriteLine(2 >= 2);

            Console.Write("2 != 2 = ");
            Console.WriteLine(2 != 2);

            Console.Write("2 == 2 = ");
            Console.WriteLine(2 == 2);

            Console.ReadLine();
        }
    }
